    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        DefaultTableModel Modelo = (DefaultTableModel) tablabuscado.getModel();
        Modelo.setNumRows(0);

        Statement st;
        ResultSet rs;
        String[] buscado = new String[4];
        try {
            st = cn.GetConection().createStatement();
            rs = st.executeQuery("select * from productos where ID='" + txtid.getText() + "' or Producto='" + txtid.getText() + "';");
            while (rs.next()) {
                buscado[0] = rs.getString(1);
                buscado[1] = rs.getString(2);
                buscado[2] = rs.getString(3);
                buscado[3] = rs.getString(4);
                Modelo.addRow(buscado);

                int cantidad = rs.getInt(4);
                if (cantidad <= 5) {
                    tablabuscado.setForeground(Color.red);
                } else {
                    tablabuscado.setForeground(Color.blue);
                }

            }

        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "El producto no esta en inventario", "Atencion", JOptionPane.WARNING_MESSAGE);
        }
        if (buscado[0] == null) {
            JOptionPane.showMessageDialog(null, "No se encuentra el producto");
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

