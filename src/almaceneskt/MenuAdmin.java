    private void btnvenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnvenderActionPerformed
        String ID = txtid.getText();
        int vender = Integer.parseInt(JOptionPane.showInputDialog("Cantidad a vender?"));
        Integer Actual = 0;
        Statement st, st2;
        ResultSet rs;
        try {
            st = cn.GetConection().createStatement();
            rs = st.executeQuery("select Cantidad from productos where ID=" + ID);
            while (rs.next()) {
                Actual = rs.getInt("Cantidad");
            }

            if (Actual < vender) {
                JOptionPane.showMessageDialog(null, "No hay suficientes", "Error!", JOptionPane.ERROR_MESSAGE);
            } else {
                int result = Actual - vender;
                st2 = cn.GetConection().createStatement();
                st2.executeUpdate("update productos set Cantidad =" + result + " where ID =" + ID + "");
                JOptionPane.showMessageDialog(null, "Producto Vendido!");
            }
        } catch (Exception e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_btnvenderActionPerformed

